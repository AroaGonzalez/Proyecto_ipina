CREATE TABLE IF NOT EXISTS AJENOS.LOCALIZACION_COMPRA (
    ID_LOCALIZACION_COMPRA BIGINT NOT NULL,
    DESCRIPCION VARCHAR(2000) NOT NULL,
    INFORMACION_ADICIONAL VARCHAR(2000) NULL,
    ID_TIPO_LOCALIZACION_COMPRA SMALLINT NOT NULL,
    ID_PAIS SMALLINT NOT NULL,
    ID_EMPRESA INTEGER NOT NULL,
    ID_CENTRO_COSTE INTEGER NULL,
    ID_DIRECCION INTEGER NULL,
    ID_LOCALIZACION INTEGER NULL,
    FECHA_ALTA TIMESTAMP NOT NULL,
    USUARIO_ALTA VARCHAR(400) NOT NULL,
    FECHA_MODIFICACION TIMESTAMP NULL,
    USUARIO_MODIFICACION VARCHAR(400) NULL,
    FECHA_BAJA TIMESTAMP NULL,
    USUARIO_BAJA VARCHAR(400) NULL,
    NUMERO_ORDEN SMALLINT NOT NULL DEFAULT 0,
    ID_CADENA SMALLINT NULL,
    ID_GRUPO_CADENA INTEGER NULL,
    PRIMARY KEY (ID_LOCALIZACION_COMPRA)
);