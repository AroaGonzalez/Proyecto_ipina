CREATE TABLE IF NOT EXISTS MAESTROS.PAIS (
    ID_PAIS SMALLINT NOT NULL,
    DESCRIPCION VARCHAR(1500) NOT NULL,
    PAIS_ISO CHARACTER(20) NOT NULL,
    CULTURA CHARACTER(100) NULL,
    COD_IBAN DECIMAL(1,0) NULL,
    NOMBRE_CORTO CHARACTER(70) NULL,
    COD_OSCE DECIMAL(3,0) NULL,
    NOMBRE_REGION VARCHAR(400) NULL,
    NOMBRE_PROVINCIA VARCHAR(400) NULL,
    LATITUD DECIMAL(8,0) NULL,
    LONGITUD DECIMAL(8,0) NULL,
    FECHA_ALTA TIMESTAMP NOT NULL,
    USUARIO_ALTA VARCHAR(200) NOT NULL,
    FECHA_MODIFICACION TIMESTAMP NULL,
    USUARIO_MODIFICACION VARCHAR(200) NULL,
    PAIS_ISO_3166 SMALLINT NULL,
    ES_7P DECIMAL(1,0) NULL DEFAULT 0,
    COD_PAIS_PDA VARCHAR(100) NULL,
    FECHA_BAJA TIMESTAMP NULL,
    PREFIJO VARCHAR(100) NULL,
    PRIMARY KEY (ID_PAIS)
);