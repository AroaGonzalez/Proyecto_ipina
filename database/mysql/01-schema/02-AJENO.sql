-- Crear la tabla AJENO
CREATE TABLE IF NOT EXISTS AJENOS.AJENO (
    ID_AJENO BIGINT NOT NULL,
    NOMBRE VARCHAR(1500) NOT NULL,
    ID_TIPO_AJENO SMALLINT NOT NULL,
    DESCRIPCION VARCHAR(1500) NULL,
    DESCRIPCION_TECNICA VARCHAR(1500) NULL,
    ID_DEPARTAMENTO_EMPRESA_GRUPO SMALLINT NOT NULL,
    ID_MAQUINA_ESTADO_AJENOS INT NOT NULL,
    REFERENCIA_AUXILIAR VARCHAR(1500) NULL,
    EXPORTACION DECIMAL(1,0) NOT NULL,
    PRECIO_PROFORMA DECIMAL(19,4) NULL,
    GESTION_LOTES DECIMAL(1,0) NOT NULL,
    GESTION_PAIS DECIMAL(1,0) NOT NULL,
    CODIFICADO DECIMAL(1,0) NOT NULL,
    CONTABILIDAD_SEGUN_PEDIDO DECIMAL(1,0) NOT NULL DEFAULT 0,
    FECHA_ALTA TIMESTAMP NOT NULL,
    USUARIO_ALTA VARCHAR(40) NOT NULL,
    FECHA_MODIFICACION TIMESTAMP NULL,
    USUARIO_MODIFICACION VARCHAR(40) NULL,
    DISPONIBLE_IMPLANTACIONES DECIMAL(1,0) NOT NULL,
    ID_CALIDAD_PRODUCTO_AJENOS BIGINT NULL,
    ID_CAMPANA_AJENOS INT NULL,
    ID_CLASIFICACION_AJENOS INT NOT NULL,
    ID_TIPO_ARTICULO_AJENO INT NULL,
    MANIPULABLE DECIMAL(1,0) NOT NULL DEFAULT 0,
    ID_CLASE_FACTURACION SMALLINT NOT NULL DEFAULT 1,
    ES_HERRAMIENTA DECIMAL(1,0) NOT NULL DEFAULT 0,
    ES_COMODIN DECIMAL(1,0) NOT NULL DEFAULT 0,
    ES_ADELANTO DECIMAL(1,0) NOT NULL DEFAULT 0,
    ID_UNIDADES_MEDIDA SMALLINT NULL,
    MARCA VARCHAR(1500) NULL,
    MODELO VARCHAR(1500) NULL,
    ES_DATOS_VALIDOS DECIMAL(1,0) NOT NULL DEFAULT 0,
    UNIDADES_CONTENIDAS DECIMAL(19,4) NULL,
    PRIMARY KEY (ID_AJENO)
);